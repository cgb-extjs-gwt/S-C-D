CREATE FUNCTION Report.LogisticCostInputCountry
(
    @cnt bigint,
    @wg bigint,
    @reactiontime bigint,
    @reactiontype bigint
)
RETURNS TABLE 
AS
RETURN (
    select Region
         , Country
         , Wg

         , Currency

         , ReactionType

         , StandardHandling
         , HighAvailabilityHandling
         , StandardDelivery
         , ExpressDelivery
         , TaxiCourierDelivery
         , ReturnDeliveryFactory

    FROM Report.LogisticCostInputCentral(coalesce(@cnt, -1), @wg, @reactiontime, @reactiontype)
)

GO

